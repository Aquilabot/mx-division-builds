<template>
	<input
		class="stat-value"
		type="number"
		:step="step"
		:max="max"
		:value="value"
		:placeholder="max"
		v-on:input="updateValue($event.target.value)"
	/>
</template>

<script>
	export default {
		name: "StatInput",
		props: {
			value: null,
			step: {
				type: Number,
				default: 0.1,
			},
			max: null,
		},
		methods: {
			updateValue: function(value) {
				this.$emit("input", Number(value));
			},
		},
	};
</script>

